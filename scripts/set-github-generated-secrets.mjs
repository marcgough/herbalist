import assert from 'node:assert/strict'
import { spawnSync } from 'node:child_process'
import { randomBytes } from 'node:crypto'
import { resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = resolve(fileURLToPath(new URL('..', import.meta.url)))
const args = process.argv.slice(2)
const argSet = new Set(args)
const expectedConfirmation = 'set-herbalisti-generated-secrets'
const secretValuePattern =
  /(sk-[A-Za-z0-9_-]{20,}|gh[pousr]_[A-Za-z0-9_]{20,}|Bearer\s+[A-Za-z0-9._-]+|-----BEGIN [A-Z ]+PRIVATE KEY-----|herbalisti_[A-Za-z0-9_-]{20,})/i

const valueAfter = (name, fallback = '') => {
  const index = args.indexOf(name)
  return index >= 0 && args[index + 1] ? args[index + 1] : fallback
}

const repository = valueAfter('--repo', 'marcgough/herbalist')
const environment = valueAfter('--environment', 'production')
const confirm = valueAfter('--confirm')
const dryRun = argSet.has('--dry-run') || argSet.has('--check')

const generatedSecrets = [
  {
    name: 'FEED_ADMIN_TOKEN',
    bytes: 32,
    purpose: 'Protects production feed refresh endpoints.',
  },
  {
    name: 'MEDIA_ADMIN_TOKEN',
    bytes: 32,
    purpose: 'Protects production media generation endpoints.',
  },
]

const ghArgsFor = (name) => ['secret', 'set', name, '--env', environment, '--repo', repository]
const commandFor = (name) => `gh ${ghArgsFor(name).join(' ')}`

const checks = [
  {
    id: 'production-environment-scope',
    status: environment === 'production' ? 'pass' : 'fail',
    detail: 'Generated tokens are scoped to the GitHub production environment.',
  },
  {
    id: 'owned-secret-names',
    status: generatedSecrets.every((secret) => ['FEED_ADMIN_TOKEN', 'MEDIA_ADMIN_TOKEN'].includes(secret.name))
      ? 'pass'
      : 'fail',
    detail: 'Only Herbalisti-owned admin token secret names are generated by this helper.',
  },
  {
    id: 'no-cloudflare-or-provider-secret-generation',
    status: generatedSecrets.every((secret) => !['CLOUDFLARE_API_TOKEN', 'CLOUDFLARE_ACCOUNT_ID', 'KIE_API_KEY'].includes(secret.name))
      ? 'pass'
      : 'fail',
    detail: 'Externally issued Cloudflare and Kie.ai credentials are not generated here.',
  },
  {
    id: 'no-command-body-values',
    status: generatedSecrets.every((secret) => !commandFor(secret.name).includes('--body')) ? 'pass' : 'fail',
    detail: 'Commands read secret values from stdin and do not place values on the command line.',
  },
]

const fail = (message) => {
  const output = {
    status: 'fail',
    dryRun,
    repository,
    environment,
    generatedSecretNames: generatedSecrets.map((secret) => secret.name),
    message,
    safeToRun:
      'Dry-run mode has no side effects. Write mode creates new random values and sends them to GitHub secret storage through stdin without printing or storing the values.',
  }
  console.error(JSON.stringify(output, null, 2))
  process.exit(1)
}

if (checks.some((check) => check.status !== 'pass')) {
  fail('Generated secret helper configuration failed local checks.')
}

if (!dryRun && confirm !== expectedConfirmation) {
  fail(`Confirmation required. Re-run with --confirm ${expectedConfirmation}.`)
}

const result = {
  status: 'pass',
  dryRun,
  repository,
  environment,
  generatedSecretNames: generatedSecrets.map((secret) => secret.name),
  externallyIssuedSecretNames: ['CLOUDFLARE_API_TOKEN', 'CLOUDFLARE_ACCOUNT_ID', 'KIE_API_KEY'],
  commands: generatedSecrets.map((secret) => commandFor(secret.name)),
  confirmationRequired: expectedConfirmation,
  checks,
  safeToRun: dryRun
    ? 'Dry-run mode validates the helper and prints command shapes only. It does not generate secret values, call GitHub, set secrets, deploy, mutate DNS, create Cloudflare resources, call paid APIs, or print secret values.'
    : 'Write mode generates new Herbalisti-owned admin token values and sends them to GitHub secret storage through stdin. It does not print, store, or log the generated values.',
}

if (!dryRun) {
  result.setSecretNames = []

  for (const secret of generatedSecrets) {
    const generatedValue = `herbalisti_${randomBytes(secret.bytes).toString('base64url')}`
    const write = spawnSync('gh', ghArgsFor(secret.name), {
      cwd: root,
      input: generatedValue,
      encoding: 'utf8',
      stdio: ['pipe', 'ignore', 'pipe'],
    })

    if (write.error || write.status !== 0) {
      fail(`Could not set ${secret.name} in GitHub ${environment} environment.`)
    }

    result.setSecretNames.push(secret.name)
  }
}

const output = JSON.stringify(result, null, 2)
assert.equal(secretValuePattern.test(output), false, 'Generated secret helper output must not contain secret values.')
console.log(output)
